class Solution {
public:

    /**************************************************************************/
    /*  Given an integer, write a function to determine if it is a power of two.
    /**************************************************************************/
    
    /**************************************************************************/
    /*  思路：2的幂只有一位是1
    /**************************************************************************/
    bool isPowerOfTwo(int n) {
        if(n <= 0) return false;
        // 位运算一定要加括号
        return !(n & (n-1));
    }
};